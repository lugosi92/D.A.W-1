package Tema_3;

public class Ejercicio_14_multiplicar {

	public static void main(String[] args) {
		
	
		
		 for (int tabla = 0; tabla <= 10; tabla++) {
			 System.out.println("Tabla" + tabla);
			for (int i = 0; i <= 10; i++) {
				System.out.println(tabla + "*" + i +"=" +  tabla * i );
				
			}
		}

	}

}
