package Tema_3;

import java.util.Scanner;

public class Ejercicio_6_lista_num_for {
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		System.out.println("Introduce un numero");
		int num = sc.nextInt();
		
		for (int i = 1; i <= num; i++) {
			System.out.println(i);
			
		}
		
		
		
	}
}
